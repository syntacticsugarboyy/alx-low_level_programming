#include "3-calc.h"

/**
 * op_add - adds numbers
 * @a: first number
 * @b: second number
 *
 * Description: A function that adds numbers
 *
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts numbers
 * @a: first number
 * @b: second number
 *
 * Description: A function that subtracts numbers
 *
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies numbers
 * @a: first number
 * @b: second number
 *
 * Description: A function that multiplies numbers
 *
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides numbers
 * @a: first number
 * @b: second number
 *
 * Description: A function that divides numbers
 *
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulos numbers
 * @a: first number
 * @b: second number
 *
 * Description: A function that modulos numbers
 *
 * Return: a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
